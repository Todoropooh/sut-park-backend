export const updateActivity = async (req, res) => {
  // (‡πÇ‡∏Ñ‡πâ‡∏î‡πÄ‡∏î‡∏¥‡∏°‡∏Ç‡∏≠‡∏á‡∏Ñ‡∏∏‡∏ì‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á‡πÅ‡∏•‡πâ‡∏ß ‡∏Å‡∏≤‡∏£‡∏•‡∏ö‡πÑ‡∏ü‡∏•‡πå‡πÉ‡∏ô 'update' ‡∏Ñ‡∏∑‡∏≠‡∏Å‡∏≤‡∏£ '‡πÅ‡∏ó‡∏ô‡∏ó‡∏µ‡πà' ‡πÑ‡∏°‡πà‡πÉ‡∏ä‡πà‡∏Å‡∏≤‡∏£ '‡∏•‡∏ö')
  try {
    const { id } = req.params;
    const { title, date, content } = req.body;
    let { imageUrl: existingImageUrlFromForm } = req.body;
    if (!title || !date || !content) return res.status(400).json({ message: "‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏£‡∏≠‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Å‡∏¥‡∏à‡∏Å‡∏£‡∏£‡∏°‡πÉ‡∏´‡πâ‡∏Ñ‡∏£‡∏ö‡∏ñ‡πâ‡∏ß‡∏ô" });

    const oldActivity = await Activity.findById(id);
    const updateData = { title, date: new Date(date), content };

    if (req.file) {
      updateData.imageUrl = `/uploads/${req.file.filename}`;
      if (oldActivity && oldActivity.imageUrl) {
        const oldImagePath = path.join(process.cwd(), oldActivity.imageUrl.substring(1));
        fs.unlink(oldImagePath, (err) => {
          if (err) console.error(`‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏•‡∏ö‡πÑ‡∏ü‡∏•‡πå‡πÄ‡∏Å‡πà‡∏≤‡πÑ‡∏î‡πâ: ${oldImagePath}`, err.message);
          else console.log(`‡∏•‡∏ö‡πÑ‡∏ü‡∏•‡πå‡πÄ‡∏Å‡πà‡∏≤‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à: ${oldImagePath}`);
        });
      }
    } else if (existingImageUrlFromForm === "") updateData.imageUrl = "";
    else if (existingImageUrlFromForm) updateData.imageUrl = existingImageUrlFromForm;

    const updatedActivity = await Activity.findByIdAndUpdate(id, updateData, { new: true });
    if (!updatedActivity) return res.status(404).json({ message: "‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏Å‡∏¥‡∏à‡∏Å‡∏£‡∏£‡∏°‡∏ô‡∏µ‡πâ" });
    res.json({ status: "success", message: "‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏Å‡∏¥‡∏à‡∏Å‡∏£‡∏£‡∏°‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à", data: updatedActivity });
  } catch (error) {
    console.error(error);
    res.status(500).json({ message: "‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î" });
  }
};

